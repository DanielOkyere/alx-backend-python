#!/usr/bin/env python3
"""
Basic annotations - concat
"""

def concat(str1: str, str2: str) -> str:
    """
    Concat
         Takes two strings as argument
    Args
       str1: first string 
       str2: second string
    Return:
        Return a concatenation of the two strings
    """
    return "{}{}".format(str1, str2)
